package NamingingConventions.ScopeChallenge;

import java.util.Scanner;

public class X {
    private int x;

    public X(int x) {
        this.x = x;
    }

    public int getX() {
        return x;
    }

    public void x() {
        for(int x = 0; x < 13; x++) {
            System.out.println(x + " ---> " + this.x * x);
        }
    }
}
